        private string ModifyTweetString(string i, string a)
        {
            string m = "";
            int index;
            if (i.Length == 0) return m;
            foreach (char c in i)
            {
                index = ALPHA.IndexOf(c);
                if (index > -1)
                {
                    m += a.Substring(index * 2)[0];
                    m += a.Substring(index * 2)[1];
                }
                else m += c;
            }
            return m;
        }
